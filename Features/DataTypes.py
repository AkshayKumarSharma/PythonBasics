class DataTypeExamples :
    def PrintDataTypes(self) : 
        runs = 12042                    # An integer
        average = 45.68                 # A floating point
        name = "Sachin Tendulkar"       # A string
        print (f'{name} scored {runs} at an average of {average}')
        print (f'Data type of runs : {type(runs)}')
        print (f'Data type of average : {type(average)}')
        print (f'Data type of name : {type(name)}')

